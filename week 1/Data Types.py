# Data Types
# Some common data types in Python:

int #(integer) → 10, -5
float #(decimal number) → 3.14, -0.5
str #(string) → "hello", 'Python'
bool #(boolean) → True, False
list #(list) → [1, 2, 3]
tuple #(immutable list) → (4, 5, 6)
dict #(dictionary) → {"name": "Alice", "age": 25}

age = 25  # int
height = 5.9  # float
is_student = True  # bool
colors = ["red", "blue", "green"]  # list
person = {"name": "Bob", "age": 30}  # dictionary

print(type(age), type(height), type(is_student), type(colors), type(person))

